package com.bank.implementation.Model;

public class Category {
    private  long Id;
    private  String name;

    public Category() {
        Id = Id;
        this.name = name;
    }

    public long getId() {
        return Id;
    }

    public void setId(long id) {
        Id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
